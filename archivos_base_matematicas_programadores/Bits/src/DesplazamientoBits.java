
public class DesplazamientoBits {

	public static void main(String[] args) {
		int numero = 41;
		int a = numero << 1;
		int b = numero >> 1;
		int c = numero << 2;
		int d = numero << 3;
		System.out.println("El valor de 41 << 1: " + a);
		System.out.println("El valor de 41 >> 1: " + b);
		System.out.println("El valor de 41 << 2: " + c);
		System.out.println("El valor de 41 << 3: " + d);
	}

}
